package JavaSessions;

public class DataType {

	public static void main(String[] args) {
		
		// primitive data type ---  int, double, char, boolean
		 
		// int data type
		int i1 = 10;
		i1 = 20;
		int i2 = 30;
		System.out.println(i1);
		 
		// double data type
		double d1 = 12.33;
		double d2 = 34.44;
		System.out.println(d1);
		
		// char data type -- only single word with single quotes
		char c1 = 'a';
		char c2 = 'b';
		System.out.println(c1);
		
		//boolean data type
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		
		// String is a class not a data type
		String s1 = "Selenium";
		String s2 = "WebDriver";
		System.out.println(s1);
		
	}
}